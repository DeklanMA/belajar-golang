package main

import "fmt"

func main() {
	var name string
	name = "deklan"
	fmt.Println(name)

	name = "deklan malik"
	fmt.Println(name)

	name = "malik akbar"
	fmt.Println(name)

	fmt.Println(len(name))
	fmt.Println(name[0]) // mengambil huruf pertama d
	fmt.Println(name[1]) // mengambil huruf kedua e
}